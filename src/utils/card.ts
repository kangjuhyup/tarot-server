export const numToType = (num: number) => {
  switch (num) {
    case 0:
      return 'Love';
    case 1:
      return 'Career';
    case 2:
      return 'Money';
    case 3:
      return 'Health';
  }
};

export const boolToForward = (isForword:boolean) => {
    if(isForword) return 'Forward';
    return 'Reverse';
}

